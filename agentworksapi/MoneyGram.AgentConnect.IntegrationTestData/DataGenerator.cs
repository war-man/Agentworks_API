        /// <summary>
        /// SSN Rules are:
        /// 
        /// AAABBCCCC
        /// 
        /// Group A is any number between 1 - 899, excluding 666
        /// Group B is any number between 1 - 99
        /// Group C is any number between 1 - 9999
        /// </summary>
        /// <returns></returns>
        public static string Ssn()
        {
            var firstGroup = GetRandomExcluding(1, 899, new List<int> { 666 });
            var secondGroup = GetRandomExcluding(1, 99);
            var thirdGroup = GetRandomExcluding(1, 9999);

            return $"{firstGroup:D3}{secondGroup:D2}{thirdGroup:D4}";
        }

        private static int GetRandomExcluding(int min, int max, List<int> invalidNumbers = null)
        {
            var rand = new Random(Guid.NewGuid().GetHashCode());
            var nextInt = rand.Next(min, max);
            return (invalidNumbers != null && invalidNumbers.Contains(nextInt))
                ? GetRandomExcluding(min, max, invalidNumbers)
                : nextInt;
        }

